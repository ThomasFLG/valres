    <h1>Bienvenue à la Maison des Ligues (M2L)</h1>
    <p>La <strong>Maison des Ligues</strong> (M2L) est un lieu unique qui accompagne les ligues sportives dans leur gestion quotidienne. Nous offrons des services dédiés aux associations sportives de la région, avec des ressources adaptées à leurs besoins. Que vous soyez une ligue affiliée ou un partenaire, notre objectif est de vous fournir un environnement de travail moderne, sécurisé et connecté.</p>

    <h2>Nos Services</h2>

    <h3>Accès Internet et Wifi</h3>
    <p>Tous les espaces de la M2L sont équipés d'un accès Internet haut débit, avec un réseau Wifi dédié pour les visiteurs. Vous bénéficiez ainsi d'une connexion fiable et rapide, idéale pour vos besoins professionnels.</p>

    <h3>Téléphonie VoIP</h3>
    <p>La M2L met à votre disposition un service de téléphonie IP. Vous pouvez ainsi louer des postes téléphoniques, bénéficiant d'une qualité de son optimale, tout en réduisant vos coûts de communication.</p>

    <h3>Affranchissement et Impression</h3>
    <p>Profitez de nos services d'affranchissement rapides et pratiques. Notre machine à affranchir est disponible pour vos besoins en volume, avec une facturation basée sur l'utilisation. Vous avez également accès à un service d'impression haute qualité dans notre salle de reprographie.</p>

    <h3>Réservation de Salles</h3>
    <p>Besoin d'un espace pour vos réunions ou événements ? Utilisez notre système de réservation en ligne pour réserver des salles. Nos espaces sont adaptés à vos besoins, que ce soit pour des réunions, des conférences ou des événements.</p>

    <h3>Serveur FTP et Ressources Documentaires</h3>
    <p>Accédez à un serveur FTP sécurisé contenant des ressources essentielles pour vos activités sportives, tels que des textes légaux, des modèles de dossiers, et des formations.</p>

    <h2>Gérer Votre Parc Informatique</h2>

    <h3>Gestion des Configurations</h3>
    <p>Nous vous offrons une gestion centralisée de vos équipements informatiques, y compris l'installation d'antivirus, la configuration réseau, ainsi que des sauvegardes régulières sur notre serveur FTP. Nos experts s'occupent de l'intégration et de la maintenance de vos postes.</p>

    <h3>Services d'Impression</h3>
    <p>Imprimez vos documents sur nos équipements haut de gamme, incluant une photocopieuse, une imprimante laser couleur et un traceur pour les affiches. Vous pourrez facilement suivre votre consommation grâce à notre système de comptage.</p>

    <h2>Services Administratifs</h2>

    <h3>Bulletins de Salaire</h3>
    <p>Le <strong>CRIB</strong> du CROSL offre un service complet d'établissement de bulletins de salaire pour les ligues et leurs clubs affiliés. Nous prenons en charge la déclaration des salaires, le calcul des charges et la gestion des paiements. Vous bénéficiez ainsi d'une solution fiable et conforme aux normes légales.</p>

    <h3>Formations pour les Bénévoles</h3>
    <p>La M2L propose des formations destinées à soutenir les bénévoles dans leur rôle au sein des ligues. De la législation à la comptabilité associative, nous vous aidons à développer vos compétences.</p>

    <h2>Nos Infrastructures</h2>

    <h3>Réseaux Sécurisés et Performants</h3>
    <p>La M2L met en place des infrastructures réseaux modernes, incluant des serveurs dédiés à l'annuaire, à l'impression, et à la gestion des configurations. Nous avons également un réseau DMZ sécurisé pour les services accessibles depuis l'extérieur.</p>

    <h3>Serveurs et Sauvegardes</h3>
    <p>Grâce à notre serveur NAS, vos données sont sauvegardées en toute sécurité. Vous pouvez compter sur une gestion centralisée et sécurisée de vos fichiers et ressources informatiques.</p>

